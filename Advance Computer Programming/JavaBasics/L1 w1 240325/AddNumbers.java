public class AddNumbers {
    public static void main(String[] args) {
        // Declare two numbers
        int num1 = 10;
        int num2 = 20;
        
        // Add the numbers
        int sum = num1 + num2;
        
        // Print the result
        System.out.println("First number: " + num1);
        System.out.println("Second number: " + num2);
        System.out.println("Sum of the numbers: " + sum);
    }
} 